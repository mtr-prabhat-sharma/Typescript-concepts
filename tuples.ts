/**
 In TypeScript, a tuple is a data structure that allows you to define an ordered 
 collection of elements, each with a specific type. 
 Tuples are similar to arrays but have a fixed length and the types of their 
 elements are known at compile time
 *

 */
let person: [string, number] = ['Jhon Doe', 32];
console.log(person)
//OR
let person1: [name:string, age:number] = ['Meera Singh', 30];
console.log(person1)